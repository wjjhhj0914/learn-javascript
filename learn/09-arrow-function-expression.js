// 하살표 함수 표현식 (Arrow Function Expression)
// ------------------------------------------------------

// 함수를 만드는 방법 3가지

// 1. 함수 선언
function showMeTheMoney() {
  console.log('Show me the money.');
}
showMeTheMoney();

// 2. 함수 표현식
let lookAtMe = function () {
  console.log('Look at me!');
};
lookAtMe();

// 3. 화살표 함수 표현식
let readABook = () => {
  console.log('Read a book!');
};
readABook();
